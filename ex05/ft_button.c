int	ft_button(int i, int j, int k)
{
	if ((j < i && i < k) || (j > i && i > k))
		return (i);
	else if ((j > i && j < k) || (j < i && j > k))
		return (j);
	else if ((k > i && k < j) || (k < i && k > j))
		return (k);
	else
		return (0);
}
